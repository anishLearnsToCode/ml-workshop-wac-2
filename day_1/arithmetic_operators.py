a = 10
b = 3

# REPL --> repeat evaluate print loop

# addition
print(a + b)

# subtraction
print(a - b)

# multiplication
print(a * b)

# division (--> float)
print(a / b)

# integer division (// --> a // b = floor(a / b)) --> int
print(a // b)

# power function (a ^ b)
print(b ** a)

# modulus operator (a % b = a - (a // b) * b) --> int (remainder)
print(a % b)
